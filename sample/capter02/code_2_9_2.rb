# AND
sprintf '%#b', (0b1010 & 0b1100)    #=> "0b10000"
# OR
sprintf '%#b', (0b1010 | 0b1100)    #=> "0b11110"
# XOR
sprintf '%#b', (0b1010 ^ 0b1100)    #=> "0b110"
# 右ビットシフト
sprintf '%#b', (0b1010 >> 1)        #=> "0b101"
# 左ビットシフト
sprintf '%#b', (0b1010 << 1)        #=> "0b10100"
# ビットごとの論理反転（NOT)  ..1 は左に１が無限に続くことを表す
sprintf '%#b', ~0b1010              #=> "0b..0101"
